package com.rsm.common.vo;

public class TrimInfoVo {
	private String trimNo;
	private String model;
	private String diverLoc;
	private String destination;
	private String swBzr;
	private String rrack;

	public String getTrimNo() {
		return trimNo;
	}

	public void setTrimNo(String trimNo) {
		this.trimNo = trimNo;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getDiverLoc() {
		return diverLoc;
	}

	public void setDiverLoc(String diverLoc) {
		this.diverLoc = diverLoc;
	}

	public String getDestination() {
		return destination;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	public String getSwBzr() {
		return swBzr;
	}

	public void setSwBzr(String swBzr) {
		this.swBzr = swBzr;
	}

	public String getRrack() {
		return rrack;
	}

	public void setRrack(String rrack) {
		this.rrack = rrack;
	}

}
